// Function to get hotel image
function get_hotel_image($hotel_id, $type = 'main', $subtype = null) {
    global $HOTEL_IMAGES;
    
    if (!isset($HOTEL_IMAGES[$hotel_id])) {
        // If hotel_id not found, use hotel 1 as default
        $hotel_id = 1;
    }
    
    if ($type === 'main') {
        return $HOTEL_IMAGES[$hotel_id]['main'];
    }
    
    if ($type === 'rooms' && $subtype) {
        return isset($HOTEL_IMAGES[$hotel_id]['rooms'][$subtype]) 
            ? $HOTEL_IMAGES[$hotel_id]['rooms'][$subtype] 
            : $HOTEL_IMAGES[$hotel_id]['rooms']['simple'];
    }
    
    if ($type === 'amenities' && $subtype) {
        return isset($HOTEL_IMAGES[$hotel_id]['amenities'][$subtype])
            ? $HOTEL_IMAGES[$hotel_id]['amenities'][$subtype]
            : null;
    }
    
    if ($type === 'gallery') {
        return $HOTEL_IMAGES[$hotel_id]['gallery'];
    }
    
    return null;
}
